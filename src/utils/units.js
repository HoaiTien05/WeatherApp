export function cToF(c) {
  return (c * 9) / 5 + 32;
}
export function fToC(f) {
  return ((f - 32) * 5) / 9;
}

export function kmhToMph(k) {
  return k / 1.609;
}
export function mphToKmh(m) {
  return m * 1.609;
}

export function mmToIn(mm) {
  return mm / 25.4;
}
export function inToMm(i) {
  return i * 25.4;
}
